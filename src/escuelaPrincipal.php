<h3>Creación de la Cartilla de Salud Bucal</h3>
<p><a href="img/pictures/consultamexicana.png" title="Otra niña super feliz!"><img src="img/pictures/consultamexicana.png" alt="Paciente" class="aligncenter" /></a></p>
